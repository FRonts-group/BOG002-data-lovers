class Pokemon {
  constructor(
    id,
    name,
    height,
    weight,
    abilities,
    moves,
    image,
    stats,
    type,
    forms,
    evolutions
  ) {
    this.id = id;
    this.name = name;
    this.height = height;
    this.weight = weight;
    this.abilities = abilities;
    this.moves = moves;
    this.image = image;
    this.stats = stats;
    this.type = type;
    this.forms = forms;
    this.evolutions = evolutions
  }
}
