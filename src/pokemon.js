class Pokemon {
  constructor(
    id,
    name,
    height,
    weight,
    abilities,
    image,
    type,
    forms
  ) {
    this.id = id;
    this.name = name;
    this.height = height;
    this.weight = weight;
    this.abilities = abilities;
    this.image = image;
    this.type = type;
    this.forms = forms;
  }
}
