class Stats {
  constructor(idPokemon, hp, attack, defense, specialAttack, specialDefense, speed) {
    this.idPokemon = idPokemon
    this.hp = hp;
    this.attack = attack;
    this.defense = defense;
    this.specialAttack = specialAttack;
    this.specialDefense = specialDefense;
    this.speed = speed;
  }
}
